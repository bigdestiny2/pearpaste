import { spawnSync } from 'node:child_process'
import { createHash } from 'node:crypto'
import { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { tmpdir } from 'node:os'
import { fileURLToPath } from 'node:url'
import { COMMANDS, SCHEMAS, UNLOCKED_NOT_REQUIRED } from '../../backend/rpc.js'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(scriptDir, '..', '..')
const args = new Set(process.argv.slice(2))
const check = args.has('--check')
const selectedPlatform = getArgValue('--platform')
const entry = 'mobile/backend/worklet.mjs'
const barePack = findBarePack()
const rpcSurface = getRpcSurface()

const bundles = [
  {
    platform: 'android',
    out: 'mobile/backend/app.android.bundle.js',
    hosts: ['android-arm64', 'android-arm', 'android-ia32', 'android-x64']
  },
  {
    platform: 'ios',
    out: 'mobile/backend/app.ios.bundle.js',
    hosts: ['ios-arm64', 'ios-arm64-simulator']
  }
].filter(bundle => !selectedPlatform || bundle.platform === selectedPlatform)

if (selectedPlatform && bundles.length === 0) {
  console.error(`Unknown platform "${selectedPlatform}". Use android or ios.`)
  process.exit(1)
}

const tmp = check ? mkdtempSync(join(tmpdir(), 'pearpaste-bare-')) : null
let failed = false

for (const bundle of bundles) {
  const hostArgs = bundle.hosts.flatMap(host => ['--host', host])
  const target = resolve(repoRoot, bundle.out)
  const out = check ? join(tmp, `${bundle.platform}.bundle.js`) : target
  const packArgs = ['--linked', ...hostArgs, '--out', out, entry]
  const result = spawnSync(barePack, packArgs, { cwd: repoRoot, stdio: 'inherit' })
  if (result.error) throw result.error
  if (result.status !== 0) process.exit(result.status || 1)

  const stamped = stampBundle(readFileSync(out, 'utf8'), bundle)
  if (check) {
    const current = existsSync(target) ? readFileSync(target, 'utf8') : null
    if (current !== stamped) {
      console.error(`${bundle.out} is stale. Run: npm --prefix mobile/pearpaste-expo run bundle:bare`)
      failed = true
    }
  } else {
    writeFileSync(target, stamped)
    console.log(`wrote ${bundle.out} (${rpcSurface.hash})`)
  }
}

if (tmp) rmSync(tmp, { recursive: true, force: true })
if (failed) process.exit(1)

function getArgValue (name) {
  const argv = process.argv.slice(2)
  const i = argv.indexOf(name)
  if (i === -1) return null
  return argv[i + 1] || ''
}

function findBarePack () {
  const candidates = [
    'mobile/pearpaste-expo/node_modules/.bin/bare-pack',
    'mobile/PearPasteMobile/node_modules/.bin/bare-pack'
  ].map(p => resolve(repoRoot, p))

  for (const candidate of candidates) {
    if (existsSync(candidate)) return candidate
  }

  console.error('bare-pack not found. Run: npm install --legacy-peer-deps in mobile/pearpaste-expo')
  process.exit(1)
}

function getRpcSurface () {
  const surface = stable({
    commands: COMMANDS,
    schemas: SCHEMAS,
    unlockedNotRequired: [...UNLOCKED_NOT_REQUIRED].sort()
  })
  const json = JSON.stringify(surface)
  return {
    commands: Object.values(COMMANDS).sort(),
    hash: createHash('sha256').update(json).digest('hex')
  }
}

function stable (value) {
  if (Array.isArray(value)) return value.map(stable)
  if (value && typeof value === 'object') {
    return Object.keys(value).sort().reduce((acc, key) => {
      acc[key] = stable(value[key])
      return acc
    }, {})
  }
  return value
}

function stampBundle (source, bundle) {
  const body = source.replace(/^\/\/ Generated by mobile\/scripts\/bundle-bare\.mjs[\s\S]*?\nmodule\.exports = /, 'module.exports = ')
  return [
    '// Generated by mobile/scripts/bundle-bare.mjs; do not edit by hand.',
    `// Platform: ${bundle.platform}`,
    `// RPC surface sha256: ${rpcSurface.hash}`,
    `// RPC commands: ${rpcSurface.commands.join(', ')}`,
    body
  ].join('\n')
}
